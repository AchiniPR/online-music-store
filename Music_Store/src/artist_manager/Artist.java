package artist_manager;

public class Artist {
	
	private int aID;
	private String aName;
	private String aDescription;
	private String aDateOfBirth;
	private String aGeneres;
	private String aLabels;
	private String aPlayRole;
	private String aActiveYears;
	
	public int getaID() {
		return aID;
	}
	public void setaID(int aID) {
		this.aID = aID;
	}
	public String getaName() {
		return aName;
	}
	public void setaName(String aName) {
		this.aName = aName;
	}
	public String getaDescription() {
		return aDescription;
	}
	public void setaDescription(String aDescription) {
		this.aDescription = aDescription;
	}
	public String getaDateOfBirth() {
		return aDateOfBirth;
	}
	public void setaDateOfBirth(String aDateOfBirth) {
		this.aDateOfBirth = aDateOfBirth;
	}
	public String getaGeneres() {
		return aGeneres;
	}
	public void setaGeneres(String aGeneres) {
		this.aGeneres = aGeneres;
	}
	public String getaLabels() {
		return aLabels;
	}
	public void setaLabels(String aLabels) {
		this.aLabels = aLabels;
	}
	public String getaPlayRole() {
		return aPlayRole;
	}
	public void setaPlayRole(String aPlayRole) {
		this.aPlayRole = aPlayRole;
	}
	public String getaActiveYears() {
		return aActiveYears;
	}
	public void setaActiveYears(String aActiveYears) {
		this.aActiveYears = aActiveYears;
	}
	
	
	
}
