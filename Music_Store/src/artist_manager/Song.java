package artist_manager;

public class Song {

	private int sID;
	private String sName;
	private int sAID;
	private int sGID;
	private String sAlbum;
	private String sReleasedDate;
	private double sPrice;
	
	public int getsID() {
		return sID;
	}
	public void setsID(int sID) {
		this.sID = sID;
	}
	public String getsName() {
		return sName;
	}
	public void setsName(String sName) {
		this.sName = sName;
	}
	public int getsAID() {
		return sAID;
	}
	public void setsAID(int sAID) {
		this.sAID = sAID;
	}
	public int getsGID() {
		return sGID;
	}
	public void setsGID(int sGID) {
		this.sGID = sGID;
	}
	public String getsAlbum() {
		return sAlbum;
	}
	public void setsAlbum(String sAlbum) {
		this.sAlbum = sAlbum;
	}
	public String getsReleasedDate() {
		return sReleasedDate;
	}
	public void setsReleasedDate(String sReleasedDate) {
		this.sReleasedDate = sReleasedDate;
	}
	public double getsPrice() {
		return sPrice;
	}
	public void setsPrice(double sPrice) {
		this.sPrice = sPrice;
	}
	

}
